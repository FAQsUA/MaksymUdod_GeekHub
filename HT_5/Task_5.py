''' 5.  Запишіть в один рядок генератор списку (числа в діапазоні від 0 до 100), 
        сума цифр кожного елемент якого буде дорівнювати 10.
        Перевірка: [19, 28, 37, 46, 55, 64, 73, 82, 91] '''

list_gen = [digit for digit in range(0,100) if sum(int(digit_) for digit_ in str(digit)) == 10]

print(list_gen)